// FILE: server/src/migrations/20260307000001-create-contact-messages.js
// PURPOSE: Creates the `contact_messages` table in PostgreSQL.
// WHY IT WAS ADDED: The ContactMessage model (server/src/models/ContactMessage.js) existed
// but had no matching migration, so the table was never created in the DB.
// Sequelize cannot query a table that doesn't exist → the admin contact page crashed with
// "relation contact_messages does not exist" (error code 42P01).

// [FLOW 1] Strict mode – prevents accidental global variables and enforces safer JS.
'use strict'

// [FLOW 2] Export a Sequelize migration object with `up` (apply) and `down` (rollback) functions.
// Next: FLOW 3 – the `up` function is what `npx sequelize-cli db:migrate` calls.
module.exports = {

  // [FLOW 3] `up` runs when the migration is applied (db:migrate).
  // queryInterface = Sequelize's DDL helper; Sequelize = type constants.
  // Next: FLOW 4 – calls createTable to build the actual DB table.
  up: async (queryInterface, Sequelize) => {

    // [FLOW 4] Tell Sequelize CLI to CREATE TABLE `contact_messages` in Postgres.
    // Why: without this table the ContactMessage model in server/src/models/ContactMessage.js
    // has nowhere to read/write, causing a 500 error on any contact-related API call.
    // Next: FLOW 5 – define the `id` primary-key column.
    await queryInterface.createTable('contact_messages', {

      // [FLOW 5] `id` – UUID primary key, auto-generated by Postgres (UUIDV4).
      // Why UUID: consistent with all other tables (users, businesses, etc.) in this project.
      // Next: FLOW 6 – foreign key linking this message to the user who sent it.
      id: {
        type: Sequelize.UUID,         // [FLOW 5a] Column data type: UUID (128-bit identifier)
        defaultValue: Sequelize.UUIDV4, // [FLOW 5b] Auto-generate a random UUID v4 on INSERT
        primaryKey: true              // [FLOW 5c] Mark as the primary key for this table
      },

      // [FLOW 6] `user_id` – foreign key → users.id (who sent the contact message).
      // WHY allowNull:false: every message must be linked to a real logged-in user.
      // WHY CASCADE: if the user is deleted, their messages are deleted automatically.
      // Next: FLOW 7 – the message subject line.
      user_id: {
        type: Sequelize.UUID,                          // [FLOW 6a] Same UUID type as users.id
        allowNull: false,                              // [FLOW 6b] Cannot be NULL – sender is required
        references: { model: 'users', key: 'id' },    // [FLOW 6c] FK → users table, id column
        onDelete: 'CASCADE'                            // [FLOW 6d] Delete messages when user is deleted
      },

      // [FLOW 7] `subject` – short title/topic of the contact message (e.g. "Refund request").
      // Next: FLOW 8 – the full message body.
      subject: {
        type: Sequelize.STRING, // [FLOW 7a] VARCHAR(255) – enough for a subject line
        allowNull: false        // [FLOW 7b] Subject is mandatory; reject blank submissions
      },

      // [FLOW 8] `message` – the actual message content (can be long, so TEXT not STRING).
      // Next: FLOW 9 – optional category tag for filtering/routing.
      message: {
        type: Sequelize.TEXT, // [FLOW 8a] TEXT = unlimited length (no VARCHAR cap)
        allowNull: false      // [FLOW 8b] Must contain a message body
      },

      // [FLOW 9] `category` – optional tag to classify the message type (e.g. 'billing', 'bug').
      // Defaults to 'general' so old code that omits this field doesn't break.
      // Next: FLOW 10 – status enum tracking admin workflow.
      category: {
        type: Sequelize.STRING, // [FLOW 9a] Free-text category label
        allowNull: true,        // [FLOW 9b] Optional – not every message needs a category
        defaultValue: 'general' // [FLOW 9c] Default value when none is provided by the sender
      },

      // [FLOW 10] `status` – admin workflow state machine for each message.
      // Values: unread → read → replied / archived.
      // WHY ENUM: restricts to valid states only; prevents bad data being stored.
      // Next: FLOW 11 – admin reply text.
      status: {
        type: Sequelize.ENUM('unread', 'read', 'replied', 'archived'), // [FLOW 10a] Only these 4 values allowed
        defaultValue: 'unread', // [FLOW 10b] Every new message starts as unread
        allowNull: false        // [FLOW 10c] Status must always be set
      },

      // [FLOW 11] `admin_reply` – the admin's written response to the message.
      // NULL until an admin actually replies (allowNull:true).
      // Next: FLOW 12 – which admin replied.
      admin_reply: {
        type: Sequelize.TEXT, // [FLOW 11a] TEXT – replies can be long
        allowNull: true       // [FLOW 11b] NULL until admin writes a reply
      },

      // [FLOW 12] `replied_by` – UUID of the admin user who wrote the reply.
      // No FK constraint here intentionally: keeps it simple – admin could be deleted.
      // Next: FLOW 13 – when the reply was sent.
      replied_by: {
        type: Sequelize.UUID, // [FLOW 12a] References an admin's user id (soft reference)
        allowNull: true       // [FLOW 12b] NULL until someone replies
      },

      // [FLOW 13] `replied_at` – timestamp of when the admin reply was sent.
      // Used to display "Replied on <date>" in the admin UI.
      // Next: FLOW 14 – optional photo attachments.
      replied_at: {
        type: Sequelize.DATE, // [FLOW 13a] TIMESTAMPTZ in Postgres
        allowNull: true       // [FLOW 13b] NULL until a reply exists
      },

      // [FLOW 14] `photos` – JSON array of photo URLs attached to the message.
      // Stored as JSON (not JSONB) to match the model definition in ContactMessage.js.
      // Next: FLOW 15 – auto-managed created_at timestamp.
      photos: {
        type: Sequelize.JSON, // [FLOW 14a] JSON column – array of URL strings
        allowNull: true,      // [FLOW 14b] Most messages won't have photos
        defaultValue: []      // [FLOW 14c] Default to empty array so JSON.parse never returns null
      },

      // [FLOW 15] `created_at` – Sequelize underscored convention requires snake_case timestamps.
      // WHY NOW(): set the DB server time on insert, not JS time (avoids clock skew).
      // Next: FLOW 16 – updated_at mirror column.
      created_at: {
        type: Sequelize.DATE,                    // [FLOW 15a] TIMESTAMPTZ
        allowNull: false,                        // [FLOW 15b] Always required
        defaultValue: Sequelize.literal('NOW()') // [FLOW 15c] Set by Postgres at INSERT time
      },

      // [FLOW 16] `updated_at` – auto-updated by Sequelize on every save().
      // Mirrors created_at pattern. Required because the model uses timestamps:true.
      // Next: FLOW 17 – close createTable, then FLOW 18 is the `down` rollback.
      updated_at: {
        type: Sequelize.DATE,                    // [FLOW 16a] TIMESTAMPTZ
        allowNull: false,                        // [FLOW 16b] Always required
        defaultValue: Sequelize.literal('NOW()') // [FLOW 16c] Set by Postgres at INSERT time
      }
    }) // [FLOW 17] End of createTable column definitions – table is now created in Postgres
  },

  // [FLOW 18] `down` runs when migration is rolled back (db:migrate:undo).
  // WHY: allows reverting this migration cleanly without manual SQL.
  // Drops the entire contact_messages table including the status ENUM type.
  down: async (queryInterface) => {
    await queryInterface.dropTable('contact_messages') // [FLOW 18a] DROP TABLE contact_messages
  }
}
